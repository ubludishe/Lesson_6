# 2. DATASET ? ????? ?? ????? ??????
class SlidingWindowDataset(Dataset):
    def __init__(self, tokenizer, text, max_length=192):
        self.tokenizer = tokenizer
        self.max_length = max_length
        self.text_tokens = tokenizer.encode(text).ids

        self.input_ids = []
        self.labels = []

        # ???????? ???? ?? ????? ??????
        for i in range(len(self.text_tokens) - max_length):
            context = self.text_tokens[i:i + max_length - 1]  # input
            target = self.text_tokens[i + 1:i + max_length]  # labels (????????)

            self.input_ids.append(context)
            self.labels.append(target)

    def __len__(self):
        return len(self.input_ids)

    def __getitem__(self, idx):
        return {
            'input_ids': torch.tensor(self.input_ids[idx], dtype=torch.long),
            'labels': torch.tensor(self.labels[idx], dtype=torch.long)
        }


